# 🟢 4. 二維陣列在舊知識點的「全面變異」
# ==========================================
st.header("⚠️ 4. 當舊觀念遇到二維陣列：三大全面變異")
st.markdown("二維陣列的結構特殊，導致我們之前學過的部分指令，在二維環境中會產生極具迷惑性的改變：")

st.error("🚨 變異一：`in` 指令只能看見「整下列」，看不見「單一元素」！")
st.code(r"""
grid = [[1, 2], [3, 4]]

print(2 in grid)      # 輸出: False (因為 grid 裡面只有 [1, 2] 和 [3, 4]，沒有獨立的數字 2)
print([1, 2] in grid) # 輸出: True (必須整下列拿去比對才找得到)
""", language="python")

st.error("🚨 變異二：`.copy()` 徹底失效，必須強迫使用 `deepcopy()`！")
st.code(r"""
import copy
matrix_a = [[1, 2], [3, 4]]

# 錯誤複製法：
matrix_b = matrix_a.copy()
matrix_b[0][0] = 99
print(matrix_a) # 輸出: [[99, 2], [3, 4]] <-- 原始資料被污染了！

# 正確二維複製法：
matrix_c = copy.deepcopy(matrix_a)
matrix_c[0][0] = 55
print(matrix_a) # 輸出: [[99, 2], [3, 4]] <-- 原始資料安全不受影響！
""", language="python")

st.error("🚨 變異三：函式傳參修改，外層子陣列連帶崩塌！")
st.code(r"""
def clear_row(arr):
    arr[0] = [0, 0] # 修改子串列物件

# 主程式
my_grid = [[1, 2], [3, 4]]
clear_row(my_grid)
print(my_grid) # 輸出: [[0, 0], [3, 4]] <-- 函式內部修改一樣會引發外部連動！
""", language="python")


# 🟢 3. 二維陣列在排序上的進階玩法
# ==========================================
st.header("⚡ 3. 二維陣列與字串陣列的排序魔術")
st.markdown("""
在二維陣列中，每個元素本身就是一個 List。當我們對它使用 `.sort()` 時，預設會**以第 0 個位置的數值**進行排序。
如果第 0 個位置相同，就會比對第 1 個位置，依此類推。
""")

st.subheader("💡 技巧 A：基本二維排序")
st.code(r"""
points = [[3, 5], [1, 9], [3, 2], [2, 8]]
points.sort()
print(points)
# 輸出: [[1, 9], [2, 8], [3, 2], [3, 5]]
# 解析: 依照 X 座標從小到大排。X 相同的 [3, 5] 與 [3, 2]，會由 Y 座標小的 [3, 2] 排在前面。
""", language="python")

st.subheader("💡 技巧 B：字串陣列 ➡️ 其實就是二維字元陣列！")
st.markdown("在 Python 中，字串可以被看作是「字元的陣列」。因此一個裝滿字串的一維陣列，在本質上的操作跟二維陣列極為相似！")
st.code(r"""
words = ["apple", "cat", "banana", "dog"]

# 利用 key=lambda 排序：依照字串的「最後一個字元」進行排序！
words.sort(key=lambda x: x[-1])
print(words)
# 輸出: ['banana', 'apple', 'dog', 'cat']
# 解析: 最後一個字元分別是 'a', 'e', 'g', 't'，依照 ASCII 碼排序！
""", language="python")


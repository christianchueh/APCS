with st.expander("🧭 知識點三點五：置物櫃的擴建與拆除——動態調整陣列 (append, pop)"):
    st.markdown("""
    前面的置物櫃長度都是固定的。但在許多 APCS 題目中，資料是走一步才算一步的。
    這時候我們就需要學會如何**「動態增加」**或**「動態減少」**置物櫃的格子！
    
    ### ➕ 1. 擴建置物櫃：`arr.append(Value)`
    * **語法**：`陣列名稱.append(要加入的資料值)`
    * **動作**：在目前陣列的**「最後面」**，強行追加一個全新的櫃子，並把資料放進去。
    * **效果**：陣列的總長度 `len(arr)` 會自動 **+1**。
    """)
    st.code("""
box = [10, 20]
box.append(30)
# box 會變成: [10, 20, 30] (30 被貼在最右邊！)
    """, language="python")

    st.markdown("""
    ### ➖ 2. 拆除置物櫃：`arr.pop(Index)`
    * **語法**：`陣列名稱.pop(櫃子號碼牌)`
    * **動作**：把指定位置的櫃子**整個拆掉並抽出來**（預設如果不寫號碼牌，就是**抽走最後一個**）。
    * **效果**：被抽走後，後面的櫃子會集體自動往前補位，陣列的總長度 `len(arr)` 會自動 **-1**。
    """)
    st.code("""
box = [50, 60, 70, 80]

removed_last = box.pop()   # 括號不寫字：抽走最後一個 (80)
# box 變成: [50, 60, 70]

removed_first = box.pop(0) # 括號寫 0：抽走最前面第 0 個 (50)
# box 變成: [60, 70] (後面的 60, 70 自動往前補位！)
    """, language="python")

